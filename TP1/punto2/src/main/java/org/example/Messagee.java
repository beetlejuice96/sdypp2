package org.example;

public class Messagee {

    int id;
    String mensaje;

    public Messagee(int id,String mensaje){
        super();
        this.id=id;
        this.mensaje=mensaje;
    }


    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
    public String getMensaje() {
        return mensaje;
    }

    public void setMensaje(String mensaje) {
        this.mensaje = mensaje;
    }



}
